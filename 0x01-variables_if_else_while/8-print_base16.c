#include <stdlib.h>
/**
 * main -main block
 * Description: prints the lowercase alphabet in reverse
 * Return: 0
 */

int main(void)

{
	int c;

	int d;

	c = 'a';
	d = 0;
	while
	(d < 10) {
		putchar(d + '0');
		d++;
	}
	while
		(c <= 'f') {
			putchar(c);
	c++;
		}
	putchar('\n');
	return (0);
}
